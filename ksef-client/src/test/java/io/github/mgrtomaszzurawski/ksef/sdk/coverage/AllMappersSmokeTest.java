    @Test
    void tokensMappers_toRawCoversEveryEnum() {
        var sdk = TokenGenerateBuilder.create(DESCRIPTION)
                .invoiceRead().invoiceWrite().credentialsRead().credentialsManage()
                .subunitManage().enforcementOperations().introspection()
                .build();
        assertNotNull(TokensMappers.toGenerateTokenRequestRaw(sdk));
        for (var v : io.github.mgrtomaszzurawski.ksef.sdk.domain.tokens.model.TokenPermissionType.values()) {
            assertNotNull(TokensMappers.toTokenPermissionTypeRaw(v));
        }
    }

    @Test
    void certificatesMappers_toRawCoversEveryEnum() {
        var enroll = CertificateEnrollBuilder
                .create("c", KsefCertificateType.AUTHENTICATION, new byte[]{1})
                .validFrom(java.time.OffsetDateTime.now()).build();
        assertNotNull(CertificatesMappers.toEnrollCertificateRequestRaw(enroll));
        var query = CertificateQueryBuilder.create()
                .serialNumber("s").name("n")
                .type(KsefCertificateType.OFFLINE).status(CertificateStatus.ACTIVE)
                .expiresAfter(java.time.OffsetDateTime.now()).build();
        assertNotNull(CertificatesMappers.toQueryCertificatesRequestRaw(query));
        for (var v : KsefCertificateType.values()) {
            assertNotNull(CertificatesMappers.toKsefCertificateTypeRaw(v));
        }
        for (var v : CertificateStatus.values()) {
            assertNotNull(CertificatesMappers.toCertificateListItemStatusRaw(v));
        }
        for (var v : CertificateRevocationReason.values()) {
            assertNotNull(CertificatesMappers.toCertificateRevocationReasonRaw(v));
        }
    }

    @Test
    void invoicingRequestMappers_allMethods() {
        var query = InvoiceQueryBuilder.seller()
                .invoicingDateFrom(java.time.OffsetDateTime.now())
                .dateTo(java.time.OffsetDateTime.now().plusDays(1))
                .ksefNumber("k").invoiceNumber("i").sellerNip(NIP)
                .onlineOnly().selfInvoicing(true).hasAttachment(true)
                .build();
        assertNotNull(InvoicingRequestMappers.toInvoiceQueryFiltersRaw(query));
        // alt branches (offline + permanent storage + issue + buyer/thirdparty)
        assertNotNull(InvoicingRequestMappers.toInvoiceQueryFiltersRaw(
                InvoiceQueryBuilder.buyer().permanentStorageDateFrom(java.time.OffsetDateTime.now()).offlineOnly().build()));
        assertNotNull(InvoicingRequestMappers.toInvoiceQueryFiltersRaw(
                InvoiceQueryBuilder.thirdParty().issueDateFrom(java.time.OffsetDateTime.now()).build()));
        assertNotNull(InvoicingRequestMappers.toInvoicingModeRaw(InvoicingMode.ONLINE));
        assertNotNull(InvoicingRequestMappers.toInvoicingModeRaw(InvoicingMode.OFFLINE));
        var send = SendInvoiceBuilder.create(new byte[]{1}, new byte[32], new byte[16])
                .offline().build();
        assertNotNull(InvoicingRequestMappers.toSendInvoiceRequestRaw(send));
    }

